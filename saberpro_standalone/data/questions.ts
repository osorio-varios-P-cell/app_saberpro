export function getQuestionsByArea(area: AreaId): Question[] {
  return QUESTIONS.filter((q) => q.area === area);
}

export function getRandomQuestions(area: AreaId, count: number): Question[] {
  const pool = getQuestionsByArea(area);
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

export function getDiagnosticQuestions(): Question[] {
  const perArea = 3;
  const result: Question[] = [];
  const areas: AreaId[] = ["matematicas", "lectura", "ciencias", "sociales", "ingles"];
  for (const area of areas) {
    const pool = getQuestionsByArea(area);
    const shuffled = [...pool].sort(() => Math.random() - 0.5);
    result.push(...shuffled.slice(0, perArea));
  }
  return result.sort(() => Math.random() - 0.5);
}

export function getICFESQuestions(area: AreaId): Question[] {
  return QUESTIONS.filter((q) => q.area === area && q.id.startsWith("icfes_"));
}

export function getQuestionStats(): Record<AreaId, { total: number; icfes: number }> {
  const areas: AreaId[] = ["matematicas", "lectura", "ciencias", "sociales", "ingles"];
  const stats: Record<string, { total: number; icfes: number }> = {};
  for (const area of areas) {
    const all = getQuestionsByArea(area);
    const icfes = all.filter((q) => q.id.startsWith("icfes_"));
    stats[area] = { total: all.length, icfes: icfes.length };
  }
  return stats as Record<AreaId, { total: number; icfes: number }>;
}
